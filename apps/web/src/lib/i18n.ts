// ── Public API ────────────────────────────────────────────────────────────────

/** Translate a key for a given language, falling back to English. */
export function t(key: TKey, lang = 'en'): string {
  const langMap = TRANSLATIONS[lang as LangCode];
  return (langMap?.[key] ?? EN[key]) || key;
}

/** React hook — reads language from user store and returns a bound `t()` function. */
export function useT(): (key: TKey) => string {
  const lang = useUserStore(s => s.profile?.language ?? 'en');
  return (key: TKey) => t(key, lang);
}
